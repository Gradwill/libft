#include "libft.h"
#include <unistd.h>

void		ft_putnbr(int n)
{
	char c;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n *= -1;
			write(1, "-", 1);
		}
		c = '0' + (n % 10);
		if (n / 10)
			ft_putnbr(n / 10);
		write(1, &c, 1);
	}
}
